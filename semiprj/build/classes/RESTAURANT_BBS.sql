DROP TABLE RESTAURANT_BBS
CASCADE CONSTRAINTS;

DROP SEQUENCE SEQ_RESTAURANT;

CREATE TABLE RESTAURANT_BBS(
                    SEQ NUMBER(10) PRIMARY KEY,
                    ID VARCHAR2(50) NOT NULL,	-- 글의 작성자

                    NAME VARCHAR2(50) NOT NULL,
                    TEL VARCHAR2(20) NOT NULL,
                    LOCATION VARCHAR2(20) NOT NULL,
                    OPERATING_TIME VARCHAR2(30) NOT NULL,
	        KINDS VARCHAR2(10) NOT NULL,
                    SCORE NUMBER(1) NOT NULL,

                    LIKECOUNT NUMBER(10) NOT NULL,
	        READCOUNT NUMBER(10) NOT NULL,

                    TITLE VARCHAR2(200) NOT NULL,
                    REVIEW VARCHAR2(4000) NOT NULL,
                    WDATE DATE NOT NULL,

                    FILENAME VARCHAR2(50) NOT NULL,
                    NEWFILENAME VARCHAR2(50) NOT NULL
);

CREATE SEQUENCE SEQ_RESTAURANT
START WITH 1
INCREMENT BY 1;

ALTER TABLE RESTAURANT_BBS
ADD CONSTRAINT FK_RESTAURANT_ID FOREIGN KEY (ID)
REFERENCES MEMBER(ID);


